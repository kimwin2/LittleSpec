"""
Regenerate Dataset: Extract prompts from OpenHermes and generate responses
using the target model (e.g., Llama-3.1-8B-Instruct).

This creates training data where the responses are generated by the target model
itself, ensuring perfect distribution alignment between the draft model and the
target model during speculative decoding.

Approach (following P-EAGLE / SpecBundle methodology):
  1. Extract user prompts from OpenHermes 2.5
  2. Generate responses using the target model (greedy, temperature=0)
  3. Save as JSONL dataset for fast loading during training

Supports two backends:
  - vLLM (default, recommended): ~100-500 samples/s via continuous batching
  - HuggingFace (fallback): ~5-20 samples/s

Usage:
    # Fast mode with vLLM (recommended):
    python prepare_regen_dataset.py \
        --model_id /path/to/Llama-3.1-8B-Instruct \
        --output_dir ./data/regen_hermes \
        --num_samples 100000

    # Fallback with HuggingFace:
    python prepare_regen_dataset.py \
        --model_id /path/to/Llama-3.1-8B-Instruct \
        --backend hf \
        --batch_size 32
"""

import argparse
import json
import os
import time

from datasets import load_dataset, load_from_disk, Dataset

from utils.misc import setup_logger

logger = setup_logger(__name__)


def extract_user_prompts(num_samples=100000, seed=42, data_root="./"):
    """Extract only user prompts from OpenHermes 2.5.

    Returns a list of chat message lists, each containing only the user turn(s).
    System prompts are preserved if present.
    """
    # Try loading from pre-downloaded cache first
    local_cache = os.path.join(data_root, "data", "openhermes_raw")
    dataset = None
    if os.path.exists(local_cache):
        try:
            logger.info(f"Loading OpenHermes from local cache: {local_cache}")
            dataset = load_from_disk(local_cache)
            logger.info(f"  Loaded {len(dataset)} conversations from local cache")
        except Exception as e:
            logger.warning(f"  Failed to load local cache: {e}")
            dataset = None

    if dataset is None:
        logger.info(f"Loading OpenHermes 2.5 from HuggingFace (sampling {num_samples})...")
        dataset = load_dataset("teknium/OpenHermes-2.5", split="train")
        logger.info(f"  Full dataset size: {len(dataset)}")

    # Sample
    if len(dataset) > num_samples:
        dataset = dataset.shuffle(seed=seed).select(range(num_samples))
        logger.info(f"  Sampled {len(dataset)} conversations")

    # Extract user-only prompts
    role_map = {"system": "system", "human": "user", "gpt": "assistant"}
    prompts = []
    skipped = 0

    for example in dataset:
        conversations = example.get("conversations", [])
        if not conversations:
            skipped += 1
            continue

        # Extract system + user turns only (drop assistant/gpt turns)
        messages = []
        for turn in conversations:
            role = role_map.get(turn.get("from", ""), None)
            content = turn.get("value", "").strip()
            if role and content:
                if role in ("system", "user"):
                    messages.append({"role": role, "content": content})
                elif role == "assistant":
                    # Stop at first assistant turn — we only want the prompt
                    break

        # Need at least a user message
        if any(m["role"] == "user" for m in messages):
            prompts.append(messages)
        else:
            skipped += 1

    logger.info(f"Extracted {len(prompts)} prompts ({skipped} skipped)")
    return prompts


# ==============================================================================
# Backend: vLLM (recommended, ~100-500 samples/s)
# ==============================================================================

def generate_responses_vllm(model_id, tokenizer, prompts, max_new_tokens=1024,
                            tensor_parallel_size=1, gpu_memory_utilization=0.85):
    """Generate responses using vLLM for maximum throughput.

    vLLM uses continuous batching + PagedAttention, giving 10-50x speedup
    over naive HuggingFace generate().
    """
    from vllm import LLM, SamplingParams

    logger.info(f"Initializing vLLM engine (tp={tensor_parallel_size}, "
                f"gpu_util={gpu_memory_utilization})...")

    llm = LLM(
        model=model_id,
        tensor_parallel_size=tensor_parallel_size,
        gpu_memory_utilization=gpu_memory_utilization,
        dtype="bfloat16",
        trust_remote_code=True,
        max_model_len=4096,
    )

    sampling_params = SamplingParams(
        temperature=0,  # Greedy — matches target model distribution exactly
        max_tokens=max_new_tokens,
        stop_token_ids=[tokenizer.eos_token_id],
    )

    # Prepare prompt strings with chat template
    logger.info("Applying chat templates to prompts...")
    prompt_texts = []
    valid_indices = []
    for i, messages in enumerate(prompts):
        try:
            text = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True
            )
            prompt_texts.append(text)
            valid_indices.append(i)
        except Exception as e:
            if i < 5:
                logger.warning(f"  Failed to apply template for sample {i}: {e}")

    logger.info(f"Prepared {len(prompt_texts)} prompts for generation")

    # Generate all at once — vLLM handles batching internally
    start_time = time.time()
    logger.info("Starting vLLM generation (all prompts submitted at once)...")
    outputs = llm.generate(prompt_texts, sampling_params)
    elapsed = time.time() - start_time

    logger.info(f"vLLM generation complete: {len(outputs)} responses in {elapsed/60:.1f}min "
                f"({len(outputs)/elapsed:.1f} samples/s)")

    # Collect results
    results = []
    for output, orig_idx in zip(outputs, valid_indices):
        response_text = output.outputs[0].text
        full_messages = list(prompts[orig_idx]) + [
            {"role": "assistant", "content": response_text}
        ]
        results.append({
            "messages": full_messages,
            "response": response_text,
        })

    return results


