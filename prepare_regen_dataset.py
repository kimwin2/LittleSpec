# Save
# ==============================================================================

def save_as_dataset(results, output_dir):
    """Save regenerated conversations as JSONL."""
    os.makedirs(output_dir, exist_ok=True)

    jsonl_path = os.path.join(output_dir, "regen_conversations.jsonl")
    with open(jsonl_path, "w", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    logger.info(f"Saved {len(results)} conversations to {jsonl_path}")

    # Also save as Arrow dataset for fast loading
    messages_str = [
        json.dumps(r["messages"], ensure_ascii=False) for r in results
    ]
    ds = Dataset.from_dict({"messages_json": messages_str})
    ds.save_to_disk(output_dir)
    logger.info(f"Saved Arrow dataset to {output_dir}")


